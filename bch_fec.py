def gf_add(a: int, b: int) -> int:
    """Galois field addition (XOR operation)"""
    if a != a & 0x3FF:
        raise ValueError("a is not a valid GF(2^10) element")
    if b != b & 0x3FF:
        raise ValueError("b is not a valid GF(2^10) element")
    return a ^ b


def gf_sub(a: int, b: int) -> int:
    """Galois field subtraction (same as addition)"""
    return gf_add(a, b)


def gf_mul(a: int, b: int) -> int:
    """Galois field multiplication using the primitive polynomial"""
    if a != a & 0x3FF:
        raise ValueError("a is not a valid GF(2^10) element")
    if b != b & 0x3FF:
        raise ValueError("b is not a valid GF(2^10) element")

    p = 0
    for _ in range(10):
        if b & 1:
            p ^= a
        a <<= 1
        if a & 0x400:
            a ^= 0x409  # Primitive polynomial for GF(2^10)
        b >>= 1
    return p


def gf_inv(a: int) -> int:
    """Galois field multiplicative inverse"""
    if a == 0:
        raise ValueError("Cannot compute inverse of zero")
    if a != a & 0x3FF:
        raise ValueError("a is not a valid GF(2^10) element")

    inv = int(INV[a])
    return inv


def gf_div(a: int, b: int) -> int:
    """Galois field division"""
    if b == 0:
        raise ValueError("Cannot divide by zero")
    if a != a & 0x3FF:
        raise ValueError("a is not a valid GF(2^10) element")
    if b != b & 0x3FF:
        raise ValueError("b is not a valid GF(2^10) element")

    return gf_mul(a, gf_inv(b))


def gf_log(a: int) -> int:
    """Galois field logarithm"""
    if a == 0:
        raise ValueError("Logarithm of zero is undefined")
    if a != a & 0x3FF:
        raise ValueError("a is not a valid GF(2^10) element")

    return int(LOG[a])


def gf_exp(exp: int) -> int:
    """Galois field exponentiation"""
    exp = exp % 1023  # Reduce exponent to range [0, 1022]
    return int(EXP[exp])


